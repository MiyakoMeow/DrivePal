"""MemoChat prompt 模板，适配车载座舱场景."""

WRITING_SYSTEM = (
    "你将看到一段共 LINE 行的任务对话，发生在用户与车载助手之间。"
    "请阅读、记忆并理解任务对话，然后在任务指引的指导下完成任务。"
)

WRITING_INSTRUCTION = (
    "\n\n```\n"
    "任务指引：\n"
    "根据任务对话，执行以下操作：\n"
    "1 - 归纳对话中所有可能的话题，使用简洁的短语描述。\n"
    "2 - 确定每个话题的对话范围。这些范围应为一组互不相交、顺序相连的首尾区间。\n"
    "3 - 用简短的句子归纳每段对话的摘要。\n"
    "4 - 仅以 JSON 格式报告话题、摘要和范围结果，使用以下指定键名："
    "'topic'、'summary'、'start'、'end'。"
    "例如，假设一段共 M 行的对话，第 1 行到第 N 行讨论「香蕉」，"
    "第 N+1 行到第 M 行讨论「芒果」，则结果为："
    "[{'topic': '香蕉', 'summary': '用户与车载助手讨论香蕉。', 'start': 1, 'end': N}, "
    "{'topic': '芒果', 'summary': '车载助手为用户带来了芒果。', 'start': N+1, 'end': M}]。\n"
    "此外，请注意以下约定：\n"
    "1 - 对于任务对话 JSON 结果中的每个元素，'end' 的值应不小于 'start' 的值，"
    "且两者均应大于 0 且不超过任务对话总行数 LINE。\n"
    "2 - 相交区间是非法的，例如 "
    "{'topic': '苹果', 'summary': '用户和车载助手分享了苹果。', 'start': K, 'end': N} "
    "与 {'topic': '梨', 'summary': '车载助手给用户送了梨。', 'start': N-2, 'end': M} 不合法。\n"
    "```\n\n"
    "任务结果："
)

